// Browser-safe extension → MIME type lookup. Mirrors Patchwork's file tool
// (patchwork-base/file/src/mime-types.ts) plus a few common binary types, so we
// avoid pulling Node's `path` into the browser bundle via `mime-types`.

const MIME_BY_EXT: Record<string, string> = {
  js: "application/javascript",
  mjs: "application/javascript",
  cjs: "application/javascript",
  jsx: "application/javascript",
  ts: "text/typescript",
  mts: "text/typescript",
  cts: "text/typescript",
  tsx: "text/typescript",
  css: "text/css",
  html: "text/html",
  htm: "text/html",
  json: "application/json",
  jsonc: "application/json",
  md: "text/markdown",
  markdown: "text/markdown",
  mdx: "text/markdown",
  py: "text/x-python",
  xml: "text/xml",
  svg: "image/svg+xml",
  yaml: "text/yaml",
  yml: "text/yaml",
  rs: "text/x-rustsrc",
  go: "text/x-go",
  rb: "text/x-ruby",
  c: "text/x-csrc",
  h: "text/x-csrc",
  cpp: "text/x-c++src",
  hpp: "text/x-c++src",
  cc: "text/x-c++src",
  cxx: "text/x-c++src",
  java: "text/x-java",
  php: "text/x-php",
  sql: "text/x-sql",
  wat: "text/x-wast",
  wast: "text/x-wast",
  txt: "text/plain",
  csv: "text/csv",
  tsv: "text/tab-separated-values",
  sh: "text/x-shellscript",
  bash: "text/x-shellscript",
  zsh: "text/x-shellscript",
  toml: "text/x-toml",
  ini: "text/x-ini",
  log: "text/plain",
  env: "text/plain",
  png: "image/png",
  jpg: "image/jpeg",
  jpeg: "image/jpeg",
  gif: "image/gif",
  webp: "image/webp",
  avif: "image/avif",
  bmp: "image/bmp",
  ico: "image/x-icon",
  pdf: "application/pdf",
  wasm: "application/wasm",
  zip: "application/zip",
  gz: "application/gzip",
  woff: "font/woff",
  woff2: "font/woff2",
  ttf: "font/ttf",
  otf: "font/otf",
  mp3: "audio/mpeg",
  wav: "audio/wav",
  mp4: "video/mp4",
  webm: "video/webm",
};

export function lookupMimeType(extension: string): string {
  const ext = extension.replace(/^\./, "").toLowerCase();
  return MIME_BY_EXT[ext] || "application/octet-stream";
}
