// Tool entry point: a card whose behavior is generated by an LLM. The user
// writes a description, hits Activate, and an agentic loop writes an effect.js
// into the card's folder which is then loaded and run against this element.
export const LlmCardTool: ToolRender = (handle, element) => {
  const dispose = render(
    () => (
      <RepoContext.Provider value={element.repo}>
        <LlmCard handle={handle as DocHandle<LlmCardDoc>} element={element} />
      </RepoContext.Provider>
    ),
    element,
  );

  return () => dispose();
};

function LlmCard(props: { handle: DocHandle<LlmCardDoc>; element: ToolElement }) {
  const [doc] = useDocument<LlmCardDoc>(() => props.handle.url);
  const status = (): LlmCardStatus => doc()?.status ?? "draft";

  // Local UI state (never persisted): which side of the card is showing, and
  // whether the orb's anchored popover (trace while running / error) is open.
  const [flipped, setFlipped] = createSignal(false);
  const [popoverOpen, setPopoverOpen] = createSignal(false);

  const hasText = () => (doc()?.description?.trim().length ?? 0) > 0;
  const orbWorking = () => status() === "generating";
  // "Pending" = there's a description but no live effect for it: either a card
  // that was never run, or one whose effect an edit just invalidated.
  const orbPending = () => status() === "draft" && hasText();
  // The blob only appears when there's a (re)run to do — pending or in flight.
  // (Failed keeps it visible so the error bubble stays reachable.)
  const orbVisible = () => orbPending() || orbWorking() || status() === "failed";
  // Morph speed: fast when working, lazy drift otherwise.
  const orbDur = () => (orbWorking() ? "2.2s" : orbPending() ? "7s" : "16s");

  let textarea: HTMLTextAreaElement | undefined;
  let abort: AbortController | undefined;
  let effectCleanup: (() => void) | void;

  // Seed the textarea as effectively uncontrolled (and re-sync on remote
  // edits), so the round-trip through automerge never resets the caret.
  createEffect(() => {
    const description = doc()?.description ?? "";
    if (textarea && textarea.value !== description) textarea.value = description;
  });

  // If the card was already activated in a previous session, re-run its effect
  // once the doc loads so the behavior is live again.
  let bootstrapped = false;
  createEffect(() => {
    const current = doc();
    if (!current || bootstrapped) return;
    bootstrapped = true;
    if (current.status === "active" && current.folderUrl) {
      void runEffect(current.folderUrl);
    }
  });

  // Turning the card to its back suspends a live effect; turning it back to the
  // front resumes it. `defer` skips the initial run — bootstrap owns the first
  // run, and `on` keeps the status/doc reads here non-reactive.
  createEffect(
    on(
      flipped,
      (isFlipped) => {
        if (status() !== "active") return;
        const folderUrl = doc()?.folderUrl;
        if (!folderUrl) return;
        if (isFlipped) disposeEffect();
        else void runEffect(folderUrl);
      },
      { defer: true },
    ),
  );

  onCleanup(() => {
    abort?.abort();
    disposeEffect();
  });

  const onInput = (event: InputEvent & { currentTarget: HTMLTextAreaElement }) => {
    const value = event.currentTarget.value;
    const prev = doc()?.status;
    props.handle.change((card) => {
      card.description = value;
      // Editing a live card disables its effect and drops back to draft, so the
      // orb returns to the corner to re-trigger generation. Same for a failed
      // card: editing clears the error and lets the user retry.
      if (card.status === "active" || card.status === "failed") {
        card.status = "draft";
        delete card.failure;
      }
    });
    if (prev === "active") disposeEffect();
    if (prev === "active" || prev === "failed") setPopoverOpen(false);
  };

  // The orb is the card's only control. What a click does depends on state:
  // draft -> kick off generation; otherwise toggle the anchored popover (the
  // live trace while running/active, or the error bubble when failed).
  const onOrbClick = () => {
    if (status() === "draft") {
      setPopoverOpen(false);
      void activate();
    } else {
      setPopoverOpen((open) => !open);
    }
  };

  const orbTitle = (): string => {
    switch (status()) {
      case "generating":
        return "Generating… click to view progress";
      case "active":
        return "Active · click to view trace";
      case "failed":
        return "Failed · click to view error";
      default:
        return "Activate";
    }
  };

  const activate = async () => {
    disposeEffect();
    abort?.abort();
    abort = new AbortController();
    const signal = abort.signal;

    props.handle.change((card) => {
      card.transcript = [];
      delete card.failure;
      card.status = "generating";
    });

    try {
      const folderUrl = await ensureFolder(props.element.repo, props.handle);
      const { gaveUp } = await runLlmLoop(
        props.element,
        props.handle,
        folderUrl,
        signal,
      );
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      if (gaveUp !== undefined) {
        props.handle.change((card) => {
          card.status = "failed";
          card.failure = gaveUp;
        });
        return;
      }
      await runEffect(folderUrl);
    } catch (err) {
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = err instanceof Error ? err.message : String(err);
      });
    }
  };

  const runEffect = async (folderUrl: AutomergeUrl) => {
    const entry = props.handle.doc()?.entry ?? "effect.js";
    try {
      const run = await loadEffect(props.element.repo, folderUrl, entry);
      disposeEffect();
      props.handle.change((card) => {
        card.status = "active";
        delete card.failure;
      });
      // If the card was turned to its back while the module loaded, keep it
      // active but leave the effect suspended until it returns to the front.
      if (flipped()) return;
      effectCleanup = run(props.element);
    } catch (err) {
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = `failed to load ${entry}: ${
          err instanceof Error ? err.message : String(err)
        }`;
      });
    }
  };

  const stop = () => {
    abort?.abort();
  };

  const disposeEffect = () => {
    if (typeof effectCleanup === "function") {
      try {
        effectCleanup();
      } catch {
        // ignore teardown errors
      }
    }
    effectCleanup = undefined;
  };

  return (
    <div
      class={`embark-llm-card embark-llm-card--${status()}`}
      classList={{ "embark-llm-card--flipped": flipped() }}
    >
      <div class="embark-llm-card__flip">
        <div class="embark-llm-card__face embark-llm-card__face--front">
          <FlipButton onFlip={() => setFlipped((value) => !value)} />

          <textarea
            ref={textarea}
            class="embark-llm-card__paper"
            placeholder="Describe what this card should do…"
            readOnly={status() === "generating"}
            spellcheck={false}
            on:input={onInput}
            // A press on the written text edits, so keep it local (the
            // canvas/embed must not read it as a drag). A press on blank paper
            // falls through so a frameless embed can be dragged by its surface.
            on:pointerdown={(event) => {
              if (pressIsOnText(event.currentTarget, event.clientX, event.clientY)) {
                event.stopPropagation();
              }
            }}
          />

          <Show when={orbVisible()}>
            <button
              type="button"
              class="embark-llm-card__orb"
              classList={{
                "embark-llm-card__orb--working": orbWorking(),
                "embark-llm-card__orb--pending": orbPending(),
              }}
              title={orbTitle()}
              aria-label={orbTitle()}
              // Don't let the orb take focus (avoids a focus ring) or let the
              // press start a frameless surface drag.
              on:mousedown={(event) => event.preventDefault()}
              on:pointerdown={(event) => event.stopPropagation()}
              on:click={onOrbClick}
            >
              <svg viewBox="0 0 200 200" xmlns="http://www.w3.org/2000/svg" aria-hidden="true">
                {/* Re-key on the duration so SMIL restarts at the new speed. */}
                <Show when={orbDur()} keyed>
                  {(dur) => (
                    <path
                      class="embark-llm-card__orb-shape"
                      transform="translate(100 100)"
                      d={ORB_BLOB_A}
                    >
                      <animate
                        attributeName="d"
                        dur={dur}
                        repeatCount="indefinite"
                        values={ORB_BLOB_VALUES}
                      />
                    </path>
                  )}
                </Show>
              </svg>
            </button>
          </Show>

          <Show when={popoverOpen() && status() === "failed"}>
            <div
              class="embark-llm-card__bubble embark-llm-card__bubble--error"
              on:pointerdown={(event) => event.stopPropagation()}
            >
              {doc()?.failure ?? "Something went wrong."}
            </div>
          </Show>

          <Show
            when={
              popoverOpen() &&
              status() !== "failed" &&
              (doc()?.transcript?.length ?? 0) > 0
            }
          >
            <div
              class="embark-llm-card__trace"
              on:pointerdown={(event) => event.stopPropagation()}
            >
              <Show when={status() === "generating"}>
                <div class="embark-llm-card__trace-head">
                  <span>generating…</span>
                  <button class="embark-llm-card__trace-stop" on:click={stop}>
                    Stop
                  </button>
                </div>
              </Show>
              <For each={doc()?.transcript ?? []}>
                {(entry) => <TranscriptBlock entry={entry} />}
              </For>
            </div>
          </Show>
        </div>

        <div class="embark-llm-card__face embark-llm-card__face--back">
          <FlipButton onFlip={() => setFlipped((value) => !value)} />
          <div class="embark-llm-card__back">{doc()?.description ?? ""}</div>
        </div>
      </div>
    </div>
  );
}

// Small corner control that turns the card over. Stops the press from being
// read as a drag and from taking focus (so no focus ring appears).
function FlipButton(props: { onFlip: () => void }) {
  return (
    <button
      type="button"
      class="embark-llm-card__flip-btn"
      title="Flip card"
      aria-label="Flip card"
      on:mousedown={(event) => event.preventDefault()}
      on:pointerdown={(event) => event.stopPropagation()}
      on:click={props.onFlip}
    >
      <FlipIcon />
    </button>
  );
}

// Two circling arrows — a "turn it over" glyph.
function FlipIcon() {
  return (
    <svg
      width="14"
      height="14"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      stroke-width="2"
      stroke-linecap="round"
      stroke-linejoin="round"
      aria-hidden="true"
    >
      <path d="M3 12a9 9 0 0 1 15-6.7L21 8" />
      <path d="M21 3v5h-5" />
      <path d="M21 12a9 9 0 0 1-15 6.7L3 16" />
      <path d="M3 21v-5h5" />
    </svg>
  );
}

// True when the press lands on the card's written text (it should edit), false
// when it lands on blank paper (a frameless embed should be able to drag from
// it). The wrapped text block is measured with a mirror element so that
// soft-wrapped lines still count as text.
function pressIsOnText(
  textarea: HTMLTextAreaElement,
  clientX: number,
  clientY: number,
): boolean {
  if (textarea.value.trim().length === 0) return false;
  const rect = textarea.getBoundingClientRect();
  const style = getComputedStyle(textarea);
  const paddingTop = Number.parseFloat(style.paddingTop) || 0;
  const y = clientY - rect.top - paddingTop + textarea.scrollTop;
  if (y < 0) return false;
  return y <= measureWrappedTextHeight(textarea, style);
}

// Height of the textarea's text once wrapped, computed via a hidden mirror that
// replicates the textarea's content-box width and text metrics.
function measureWrappedTextHeight(
  textarea: HTMLTextAreaElement,
  style: CSSStyleDeclaration,
): number {
  const paddingX =
    (Number.parseFloat(style.paddingLeft) || 0) +
    (Number.parseFloat(style.paddingRight) || 0);
  const mirror = document.createElement("div");
  Object.assign(mirror.style, {
    position: "absolute",
    top: "0",
    left: "-9999px",
    visibility: "hidden",
    pointerEvents: "none",
    boxSizing: "content-box",
    whiteSpace: "pre-wrap",
    overflowWrap: style.overflowWrap,
    wordBreak: style.wordBreak,
    width: `${textarea.clientWidth - paddingX}px`,
    fontFamily: style.fontFamily,
    fontSize: style.fontSize,
    fontWeight: style.fontWeight,
    fontStyle: style.fontStyle,
    lineHeight: style.lineHeight,
    letterSpacing: style.letterSpacing,
  });
  // A trailing newline renders an extra empty row in the textarea; mirror it.
  const text = textarea.value;
  mirror.textContent = text.endsWith("\n") ? `${text}\u200b` : text;
  document.body.appendChild(mirror);
  const height = mirror.offsetHeight;
  mirror.remove();
  return height;
}

function TranscriptBlock(props: { entry: TranscriptEntry }) {
  return (
    <Show
      when={props.entry.type === "script" ? props.entry : undefined}
      fallback={
        <div class="embark-llm-card__text-block">
          {props.entry.type === "text" ? props.entry.content : ""}
        </div>
      }
    >
      {(script) => (
        <div class="embark-llm-card__script">
          <Show when={script().description}>
            <div class="embark-llm-card__script-desc">{script().description}</div>
          </Show>
          <pre class="embark-llm-card__code">{script().code}</pre>
          <Show when={script().output}>
            <pre class="embark-llm-card__output">{script().output}</pre>
          </Show>
          <Show when={script().error}>
            <pre class="embark-llm-card__error">{script().error}</pre>
          </Show>
        </div>
      )}
    </Show>
  );
}

