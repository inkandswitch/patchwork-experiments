import type { AutomergeUrl, DocHandle } from "@automerge/automerge-repo";
import type { ToolElement, ToolRender } from "@inkandswitch/patchwork-plugins";
import { For, Show, createEffect, createSignal, onCleanup } from "solid-js";
import { render } from "solid-js/web";
import { RepoContext, useDocument } from "solid-automerge";
import { loadEffect } from "./effect-loader";
import { ensureFolder } from "./folder";
import { runLlmLoop } from "./loop";
import type { LlmCardDoc, LlmCardStatus, TranscriptEntry } from "./types";
import "./llm-card.css";

// Tool entry point: a card whose behavior is generated by an LLM. The user
// writes a description, hits Activate, and an agentic loop writes an effect.js
// into the card's folder which is then loaded and run against this element.
export const LlmCardTool: ToolRender = (handle, element) => {
  const dispose = render(
    () => (
      <RepoContext.Provider value={element.repo}>
        <LlmCard handle={handle as DocHandle<LlmCardDoc>} element={element} />
      </RepoContext.Provider>
    ),
    element,
  );

  return () => dispose();
};

function LlmCard(props: { handle: DocHandle<LlmCardDoc>; element: ToolElement }) {
  const [doc] = useDocument<LlmCardDoc>(() => props.handle.url);
  const status = (): LlmCardStatus => doc()?.status ?? "draft";

  let textarea: HTMLTextAreaElement | undefined;
  let abort: AbortController | undefined;
  let effectCleanup: (() => void) | void;

  // Seed the textarea as effectively uncontrolled (and re-sync on remote
  // edits), so the round-trip through automerge never resets the caret.
  createEffect(() => {
    const description = doc()?.description ?? "";
    if (textarea && textarea.value !== description) textarea.value = description;
  });

  // If the card was already activated in a previous session, re-run its effect
  // once the doc loads so the behavior is live again.
  let bootstrapped = false;
  createEffect(() => {
    const current = doc();
    if (!current || bootstrapped) return;
    bootstrapped = true;
    if (current.status === "active" && current.folderUrl) {
      void runEffect(current.folderUrl);
    }
  });

  onCleanup(() => {
    abort?.abort();
    disposeEffect();
  });

  const onInput = (event: InputEvent & { currentTarget: HTMLTextAreaElement }) => {
    const value = event.currentTarget.value;
    props.handle.change((card) => {
      card.description = value;
    });
  };

  const activate = async () => {
    disposeEffect();
    abort?.abort();
    abort = new AbortController();
    const signal = abort.signal;

    props.handle.change((card) => {
      card.transcript = [];
      delete card.failure;
      card.status = "generating";
    });

    try {
      const folderUrl = await ensureFolder(props.element.repo, props.handle);
      const { gaveUp } = await runLlmLoop(
        props.element,
        props.handle,
        folderUrl,
        signal,
      );
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      if (gaveUp !== undefined) {
        props.handle.change((card) => {
          card.status = "failed";
          card.failure = gaveUp;
        });
        return;
      }
      await runEffect(folderUrl);
    } catch (err) {
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = err instanceof Error ? err.message : String(err);
      });
    }
  };

  const runEffect = async (folderUrl: AutomergeUrl) => {
    const entry = props.handle.doc()?.entry ?? "effect.js";
    try {
      const run = await loadEffect(props.element.repo, folderUrl, entry);
      disposeEffect();
      effectCleanup = run(props.element);
      props.handle.change((card) => {
        card.status = "active";
        delete card.failure;
      });
    } catch (err) {
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = `failed to load ${entry}: ${
          err instanceof Error ? err.message : String(err)
        }`;
      });
    }
  };

  const stop = () => {
    abort?.abort();
  };

  const disposeEffect = () => {
    if (typeof effectCleanup === "function") {
      try {
        effectCleanup();
      } catch {
        // ignore teardown errors
      }
    }
    effectCleanup = undefined;
  };

  return (
    <div class={`embark-llm-card embark-llm-card--${status()}`}>
      <div class="embark-llm-card__header">
        <span class="embark-llm-card__dot" />
        <span class="embark-llm-card__title">LLM Card</span>
        <span class="embark-llm-card__badge">{statusLabel(status())}</span>
      </div>

      <textarea
        ref={textarea}
        class="embark-llm-card__text"
        placeholder="Describe what this card should do… e.g. 'List birds recently seen near the map and drop a pin for each.'"
        readOnly={status() === "generating"}
        on:input={onInput}
      />

      <div class="embark-llm-card__controls">
        <Show when={status() === "generating"}>
          <button class="embark-llm-card__btn" on:click={stop}>
            Stop
          </button>
        </Show>
        <Show when={status() !== "generating"}>
          <button
            class="embark-llm-card__btn embark-llm-card__btn--primary"
            on:click={() => void activate()}
          >
            {activateLabel(status())}
          </button>
        </Show>
      </div>

      <Show when={doc()?.failure}>
        <div class="embark-llm-card__failure">{doc()?.failure}</div>
      </Show>

      <Show when={(doc()?.transcript?.length ?? 0) > 0}>
        <div class="embark-llm-card__transcript">
          <For each={doc()?.transcript ?? []}>
            {(entry) => <TranscriptBlock entry={entry} />}
          </For>
        </div>
      </Show>
    </div>
  );
}

function TranscriptBlock(props: { entry: TranscriptEntry }) {
  return (
    <Show
      when={props.entry.type === "script" ? props.entry : undefined}
      fallback={
        <div class="embark-llm-card__text-block">
          {props.entry.type === "text" ? props.entry.content : ""}
        </div>
      }
    >
      {(script) => (
        <div class="embark-llm-card__script">
          <Show when={script().description}>
            <div class="embark-llm-card__script-desc">{script().description}</div>
          </Show>
          <pre class="embark-llm-card__code">{script().code}</pre>
          <Show when={script().output}>
            <pre class="embark-llm-card__output">{script().output}</pre>
          </Show>
          <Show when={script().error}>
            <pre class="embark-llm-card__error">{script().error}</pre>
          </Show>
        </div>
      )}
    </Show>
  );
}

function statusLabel(status: LlmCardStatus): string {
  switch (status) {
    case "draft":
      return "draft";
    case "generating":
      return "generating…";
    case "active":
      return "active";
    case "failed":
      return "failed";
  }
}

function activateLabel(status: LlmCardStatus): string {
  switch (status) {
    case "active":
      return "Regenerate";
    case "failed":
      return "Retry";
    default:
      return "Activate";
  }
}
