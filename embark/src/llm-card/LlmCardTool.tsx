import type { AutomergeUrl, DocHandle } from "@automerge/automerge-repo";
import type { ToolElement, ToolRender } from "@inkandswitch/patchwork-plugins";
import { Show, createEffect, createSignal, on, onCleanup } from "solid-js";
import { render } from "solid-js/web";
import { RepoContext, useDocument } from "solid-automerge";
import { loadEffect } from "./effect-loader";
import { ensureFolder, ensureSpec } from "./folder";
import { runLlmLoop } from "./loop";
import type { LlmCardDoc, LlmCardStatus } from "./types";
import "./llm-card.css";

// Tool entry point: a card whose behavior is generated by an LLM. The user
// writes a description, hits Activate, and an agentic loop writes an effect.js
// into the card's folder which is then loaded and run against this element.
export const LlmCardTool: ToolRender = (handle, element) => {
  const dispose = render(
    () => (
      <RepoContext.Provider value={element.repo}>
        <LlmCard handle={handle as DocHandle<LlmCardDoc>} element={element} />
      </RepoContext.Provider>
    ),
    element,
  );

  return () => dispose();
};

function LlmCard(props: { handle: DocHandle<LlmCardDoc>; element: ToolElement }) {
  const [doc] = useDocument<LlmCardDoc>(() => props.handle.url);
  const status = (): LlmCardStatus => doc()?.status ?? "draft";

  // Local UI state (never persisted): which side of the card is showing. The
  // spec / code inspector now lives in its own tool (see ../inspect).
  const [flipped, setFlipped] = createSignal(false);

  const hasText = () => (doc()?.description?.trim().length ?? 0) > 0;
  // The action rail shows "make real" when there's an un-run description (a
  // fresh draft, or one a failed run left behind).
  const canActivate = () =>
    hasText() && (status() === "draft" || status() === "failed");

  let textarea: HTMLTextAreaElement | undefined;
  let abort: AbortController | undefined;
  let effectCleanup: (() => void) | void;

  // Seed the textarea as effectively uncontrolled (and re-sync on remote
  // edits), so the round-trip through automerge never resets the caret.
  createEffect(() => {
    const description = doc()?.description ?? "";
    if (textarea && textarea.value !== description) textarea.value = description;
  });

  // If the card was already activated in a previous session, re-run its effect
  // once the doc loads so the behavior is live again.
  let bootstrapped = false;
  createEffect(() => {
    const current = doc();
    if (!current || bootstrapped) return;
    bootstrapped = true;
    if (current.status === "active" && current.folderUrl) {
      void runEffect(current.folderUrl);
    }
  });

  // Turning the card to its back suspends a live effect; turning it back to the
  // front resumes it. `defer` skips the initial run — bootstrap owns the first
  // run, and `on` keeps the status/doc reads here non-reactive.
  createEffect(
    on(
      flipped,
      (isFlipped) => {
        if (status() !== "active") return;
        const folderUrl = doc()?.folderUrl;
        if (!folderUrl) return;
        if (isFlipped) disposeEffect();
        else void runEffect(folderUrl);
      },
      { defer: true },
    ),
  );

  onCleanup(() => {
    abort?.abort();
    disposeEffect();
  });

  const onInput = (event: InputEvent & { currentTarget: HTMLTextAreaElement }) => {
    const value = event.currentTarget.value;
    const prev = doc()?.status;
    props.handle.change((card) => {
      card.description = value;
      // Editing a live card disables its effect and drops back to draft, so the
      // orb returns to the corner to re-trigger generation. Same for a failed
      // card: editing clears the error and lets the user retry.
      if (card.status === "active" || card.status === "failed") {
        card.status = "draft";
        delete card.failure;
      }
    });
    if (prev === "active") disposeEffect();
  };

  const activate = async () => {
    disposeEffect();
    abort?.abort();
    abort = new AbortController();
    const signal = abort.signal;

    props.handle.change((card) => {
      card.transcript = [];
      delete card.failure;
      card.status = "generating";
    });

    try {
      const folderUrl = await ensureFolder(props.element.repo, props.handle);
      const specUrl = await ensureSpec(props.element.repo, props.handle);
      const { gaveUp } = await runLlmLoop(
        props.element,
        props.handle,
        folderUrl,
        specUrl,
        signal,
      );
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      if (gaveUp !== undefined) {
        props.handle.change((card) => {
          card.status = "failed";
          card.failure = gaveUp;
        });
        return;
      }
      await runEffect(folderUrl);
    } catch (err) {
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = err instanceof Error ? err.message : String(err);
      });
    }
  };

  const runEffect = async (folderUrl: AutomergeUrl) => {
    const entry = props.handle.doc()?.entry ?? "effect.js";
    try {
      const run = await loadEffect(props.element.repo, folderUrl, entry);
      disposeEffect();
      props.handle.change((card) => {
        card.status = "active";
        delete card.failure;
      });
      // If the card was turned to its back while the module loaded, keep it
      // active but leave the effect suspended until it returns to the front.
      if (flipped()) return;
      effectCleanup = run(props.element);
    } catch (err) {
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = `failed to load ${entry}: ${
          err instanceof Error ? err.message : String(err)
        }`;
      });
    }
  };

  const stop = () => {
    abort?.abort();
  };

  const disposeEffect = () => {
    if (typeof effectCleanup === "function") {
      try {
        effectCleanup();
      } catch {
        // ignore teardown errors
      }
    }
    effectCleanup = undefined;
  };

  return (
    <div
      class={`embark-llm-card embark-llm-card--${status()}`}
      classList={{ "embark-llm-card--flipped": flipped() }}
    >
      <div class="embark-llm-card__flip">
        <div class="embark-llm-card__face embark-llm-card__face--front">
          <FlipButton onFlip={() => setFlipped((value) => !value)} />

          {/* Blank upper area — what makes the card read as a playing card. */}
          <div class="embark-llm-card__top" />

          <textarea
            ref={textarea}
            class="embark-llm-card__desc"
            placeholder="Describe what this card should do…"
            readOnly={status() === "generating"}
            spellcheck={false}
            on:input={onInput}
            // The description box is for editing, never dragging: always keep
            // the press local so the canvas surface can't grab the pointer
            // (select/drag). Drag the card from its blank upper area instead.
            on:pointerdown={(event) => event.stopPropagation()}
          />

          <Show when={status() === "failed"}>
            <div
              class="embark-llm-card__bubble embark-llm-card__bubble--error"
              on:pointerdown={(event) => event.stopPropagation()}
            >
              {doc()?.failure ?? "Something went wrong."}
            </div>
          </Show>
        </div>

        <div class="embark-llm-card__face embark-llm-card__face--back">
          <FlipButton onFlip={() => setFlipped((value) => !value)} />
          <div class="embark-llm-card__top" />
          <div class="embark-llm-card__desc embark-llm-card__desc--back">
            {doc()?.description ?? ""}
          </div>
        </div>
      </div>

      {/* The action rail sits below the card, outside its border. */}
      <Show when={canActivate()}>
        <button
          type="button"
          class="embark-llm-card__action"
          on:mousedown={(event) => event.preventDefault()}
          on:pointerdown={(event) => event.stopPropagation()}
          on:click={() => void activate()}
        >
          make real
        </button>
      </Show>
      <Show when={status() === "generating"}>
        <button
          type="button"
          class="embark-llm-card__action"
          on:mousedown={(event) => event.preventDefault()}
          on:pointerdown={(event) => event.stopPropagation()}
          on:click={stop}
        >
          stop
        </button>
      </Show>
    </div>
  );
}

// Small corner control that turns the card over. Stops the press from being
// read as a drag and from taking focus (so no focus ring appears).
function FlipButton(props: { onFlip: () => void }) {
  return (
    <button
      type="button"
      class="embark-llm-card__flip-btn"
      title="Flip card"
      aria-label="Flip card"
      on:mousedown={(event) => event.preventDefault()}
      on:pointerdown={(event) => event.stopPropagation()}
      on:click={props.onFlip}
    >
      <FlipIcon />
    </button>
  );
}

// Two circling arrows — a "turn it over" glyph.
function FlipIcon() {
  return (
    <svg
      width="14"
      height="14"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      stroke-width="2"
      stroke-linecap="round"
      stroke-linejoin="round"
      aria-hidden="true"
    >
      <path d="M3 12a9 9 0 0 1 15-6.7L21 8" />
      <path d="M21 3v5h-5" />
      <path d="M21 12a9 9 0 0 1-15 6.7L3 16" />
      <path d="M3 21v-5h5" />
    </svg>
  );
}

