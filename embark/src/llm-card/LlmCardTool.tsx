import type { AutomergeUrl, DocHandle } from "@automerge/automerge-repo";
import type { ToolElement, ToolRender } from "@inkandswitch/patchwork-plugins";
import { For, Show, createEffect, createSignal, onCleanup } from "solid-js";
import { render } from "solid-js/web";
import { RepoContext, useDocument } from "solid-automerge";
import { loadEffect } from "./effect-loader";
import { ensureFolder } from "./folder";
import { runLlmLoop } from "./loop";
import type { LlmCardDoc, LlmCardStatus, TranscriptEntry } from "./types";
import "./llm-card.css";

// Tool entry point: a card whose behavior is generated by an LLM. The user
// writes a description, hits Activate, and an agentic loop writes an effect.js
// into the card's folder which is then loaded and run against this element.
export const LlmCardTool: ToolRender = (handle, element) => {
  const dispose = render(
    () => (
      <RepoContext.Provider value={element.repo}>
        <LlmCard handle={handle as DocHandle<LlmCardDoc>} element={element} />
      </RepoContext.Provider>
    ),
    element,
  );

  return () => dispose();
};

function LlmCard(props: { handle: DocHandle<LlmCardDoc>; element: ToolElement }) {
  const [doc] = useDocument<LlmCardDoc>(() => props.handle.url);
  const status = (): LlmCardStatus => doc()?.status ?? "draft";

  // Local UI state (never persisted): is the textarea focused, and is the
  // orb's anchored popover (trace while running/active, error when failed) open.
  const [focused, setFocused] = createSignal(false);
  const [popoverOpen, setPopoverOpen] = createSignal(false);

  const hasText = () => (doc()?.description?.trim().length ?? 0) > 0;
  // The orb sits in the bottom-right "ready" corner only while a fresh draft is
  // being written; once it activates it floats up to the top-right.
  const orbInCorner = () => status() === "draft";
  // Draft orb is a focus affordance (hides on blur); the running/active/failed
  // orb is the card's persistent state indicator, so it stays put.
  const orbVisible = () =>
    (status() === "draft" && hasText() && focused()) ||
    status() === "generating" ||
    status() === "active" ||
    status() === "failed";

  let textarea: HTMLTextAreaElement | undefined;
  let abort: AbortController | undefined;
  let effectCleanup: (() => void) | void;

  // Seed the textarea as effectively uncontrolled (and re-sync on remote
  // edits), so the round-trip through automerge never resets the caret.
  createEffect(() => {
    const description = doc()?.description ?? "";
    if (textarea && textarea.value !== description) textarea.value = description;
  });

  // If the card was already activated in a previous session, re-run its effect
  // once the doc loads so the behavior is live again.
  let bootstrapped = false;
  createEffect(() => {
    const current = doc();
    if (!current || bootstrapped) return;
    bootstrapped = true;
    if (current.status === "active" && current.folderUrl) {
      void runEffect(current.folderUrl);
    }
  });

  onCleanup(() => {
    abort?.abort();
    disposeEffect();
  });

  const onInput = (event: InputEvent & { currentTarget: HTMLTextAreaElement }) => {
    const value = event.currentTarget.value;
    const prev = doc()?.status;
    props.handle.change((card) => {
      card.description = value;
      // Editing a live card disables its effect and drops back to draft, so the
      // orb returns to the corner to re-trigger generation. Same for a failed
      // card: editing clears the error and lets the user retry.
      if (card.status === "active" || card.status === "failed") {
        card.status = "draft";
        delete card.failure;
      }
    });
    if (prev === "active") disposeEffect();
    if (prev === "active" || prev === "failed") setPopoverOpen(false);
  };

  // The orb is the card's only control. What a click does depends on state:
  // draft -> kick off generation; otherwise toggle the anchored popover (the
  // live trace while running/active, or the error bubble when failed).
  const onOrbClick = () => {
    if (status() === "draft") {
      setPopoverOpen(false);
      void activate();
    } else {
      setPopoverOpen((open) => !open);
    }
  };

  const orbTitle = (): string => {
    switch (status()) {
      case "generating":
        return "Generating… click to view progress";
      case "active":
        return "Active · click to view trace";
      case "failed":
        return "Failed · click to view error";
      default:
        return "Activate";
    }
  };

  const activate = async () => {
    disposeEffect();
    abort?.abort();
    abort = new AbortController();
    const signal = abort.signal;

    props.handle.change((card) => {
      card.transcript = [];
      delete card.failure;
      card.status = "generating";
    });

    try {
      const folderUrl = await ensureFolder(props.element.repo, props.handle);
      const { gaveUp } = await runLlmLoop(
        props.element,
        props.handle,
        folderUrl,
        signal,
      );
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      if (gaveUp !== undefined) {
        props.handle.change((card) => {
          card.status = "failed";
          card.failure = gaveUp;
        });
        return;
      }
      await runEffect(folderUrl);
    } catch (err) {
      if (signal.aborted) {
        props.handle.change((card) => {
          card.status = "draft";
        });
        return;
      }
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = err instanceof Error ? err.message : String(err);
      });
    }
  };

  const runEffect = async (folderUrl: AutomergeUrl) => {
    const entry = props.handle.doc()?.entry ?? "effect.js";
    try {
      const run = await loadEffect(props.element.repo, folderUrl, entry);
      disposeEffect();
      effectCleanup = run(props.element);
      props.handle.change((card) => {
        card.status = "active";
        delete card.failure;
      });
    } catch (err) {
      props.handle.change((card) => {
        card.status = "failed";
        card.failure = `failed to load ${entry}: ${
          err instanceof Error ? err.message : String(err)
        }`;
      });
    }
  };

  const stop = () => {
    abort?.abort();
  };

  const disposeEffect = () => {
    if (typeof effectCleanup === "function") {
      try {
        effectCleanup();
      } catch {
        // ignore teardown errors
      }
    }
    effectCleanup = undefined;
  };

  return (
    <div class={`embark-llm-card embark-llm-card--${status()}`}>
      <textarea
        ref={textarea}
        class="embark-llm-card__paper"
        placeholder="Describe what this card should do…"
        readOnly={status() === "generating"}
        on:input={onInput}
        on:focus={() => setFocused(true)}
        on:blur={() => setFocused(false)}
        // Editing the text must not be read as a frameless-embed surface drag,
        // so keep pointer presses local to the textarea.
        on:pointerdown={(event) => event.stopPropagation()}
      />

      <Show when={orbVisible()}>
        <button
          type="button"
          class="embark-llm-card__orb"
          classList={{
            "embark-llm-card__orb--corner": orbInCorner(),
            "embark-llm-card__orb--floating": !orbInCorner(),
          }}
          title={orbTitle()}
          aria-label={orbTitle()}
          // Keep the textarea focused through the click so the draft orb does
          // not vanish (it is focus-gated) before the handler runs, and don't
          // let the press start a frameless-embed surface drag.
          on:mousedown={(event) => event.preventDefault()}
          on:pointerdown={(event) => event.stopPropagation()}
          on:click={onOrbClick}
        />
      </Show>

      <Show when={popoverOpen() && status() === "failed"}>
        <div
          class="embark-llm-card__bubble embark-llm-card__bubble--error"
          on:pointerdown={(event) => event.stopPropagation()}
        >
          {doc()?.failure ?? "Something went wrong."}
        </div>
      </Show>

      <Show
        when={
          popoverOpen() &&
          status() !== "failed" &&
          (doc()?.transcript?.length ?? 0) > 0
        }
      >
        <div
          class="embark-llm-card__trace"
          on:pointerdown={(event) => event.stopPropagation()}
        >
          <Show when={status() === "generating"}>
            <div class="embark-llm-card__trace-head">
              <span>generating…</span>
              <button class="embark-llm-card__trace-stop" on:click={stop}>
                Stop
              </button>
            </div>
          </Show>
          <For each={doc()?.transcript ?? []}>
            {(entry) => <TranscriptBlock entry={entry} />}
          </For>
        </div>
      </Show>
    </div>
  );
}

function TranscriptBlock(props: { entry: TranscriptEntry }) {
  return (
    <Show
      when={props.entry.type === "script" ? props.entry : undefined}
      fallback={
        <div class="embark-llm-card__text-block">
          {props.entry.type === "text" ? props.entry.content : ""}
        </div>
      }
    >
      {(script) => (
        <div class="embark-llm-card__script">
          <Show when={script().description}>
            <div class="embark-llm-card__script-desc">{script().description}</div>
          </Show>
          <pre class="embark-llm-card__code">{script().code}</pre>
          <Show when={script().output}>
            <pre class="embark-llm-card__output">{script().output}</pre>
          </Show>
          <Show when={script().error}>
            <pre class="embark-llm-card__error">{script().error}</pre>
          </Show>
        </div>
      )}
    </Show>
  );
}

