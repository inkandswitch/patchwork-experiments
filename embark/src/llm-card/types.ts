import type { AutomergeUrl, Repo } from "@automerge/automerge-repo";
import type { ToolElement } from "@inkandswitch/patchwork-plugins";
import type { accept, subscribe } from "@inkandswitch/patchwork-providers";

// An LLM card: the user writes a plain-language `description`, hits Activate,
// and an LLM agentic loop generates two things — a dependency-free `effect.js`
// into the card's own automerge folder, and a human-readable markdown `spec`.
// The card then loads the file via the service worker and runs it against the
// card's element, where it hooks into the canvas providers.
export type LlmCardDoc = {
  "@patchwork": { type: "llm-card" };
  // The user's paragraph describing the desired effect.
  description: string;
  status: LlmCardStatus;
  // The folder doc that holds generated files; created on first Activate.
  folderUrl?: AutomergeUrl;
  // A standalone markdown doc describing, in plain language, what the card
  // does. Generated by the loop and freely editable by the user.
  specUrl?: AutomergeUrl;
  // The module the loader imports and runs (default "effect.js").
  entry: string;
  // The text/script/result log of the generation loop, shown in the UI.
  transcript: TranscriptEntry[];
  // A giveUp reason or fatal error, set when `status` is "failed".
  failure?: string;
  config: LlmCardConfig;
};

export type LlmCardStatus = "draft" | "generating" | "active" | "failed";

export type LlmCardConfig = {
  apiUrl: string;
  model: string;
};

// One entry in the generation transcript. Mirrors llm-canvas's OutputBlock: a
// streamed text region, or a script the loop evaluated (with its captured
// output/error).
export type TranscriptEntry =
  | { type: "text"; content: string }
  | {
      type: "script";
      code: string;
      description?: string;
      output?: string;
      error?: string;
    };

// A folder doc (patchwork filesystem "folder" strategy): a list of named
// child docs. The service worker matches a path segment against `docs[].name`
// and follows the link. `lastSyncAt` is a free field we bump to advance the
// folder's heads (cache-busting) without changing any file.
export type FolderDoc = {
  "@patchwork": { type: "folder"; title?: string };
  title: string;
  docs: DocLink[];
  lastSyncAt?: number;
};

// One entry in a folder's `docs` list, pointing at a child doc by url.
export type DocLink = {
  name: string;
  type: string;
  url: AutomergeUrl;
};

// A leaf file served by the service worker; `mimeType` becomes the response
// content-type so a js file loads as a real ES module. Shaped like patchwork's
// UnixFileEntry plus the `@patchwork` metadata the file viewer expects.
export type FileDoc = {
  "@patchwork": { type: "file"; title?: string };
  name: string;
  extension: string;
  mimeType: string;
  content: string;
};

// The standalone markdown doc that backs a card's spec.
export type MarkdownDoc = {
  "@patchwork": { type: "markdown" };
  content: string;
};

export type ChatMessage = {
  role: "system" | "user" | "assistant";
  content: string;
};

// Emitted by the streaming parser (see parser.ts).
export type ParsedBlock =
  | { id: number; type: "text"; content: string; complete: boolean }
  | {
      id: number;
      type: "script";
      code: string;
      description?: string;
      complete: boolean;
    };

// Console captured during script evaluation, replayed back into the transcript.
export type CapturedConsole = {
  log: (...args: unknown[]) => void;
  error: (...args: unknown[]) => void;
  warn: (...args: unknown[]) => void;
  info: (...args: unknown[]) => void;
  flush: () => string;
};

// The eval-scope API handed to the LLM loop's <script> blocks while it
// generates. NOTE: the activated effect is NOT handed this object - it receives
// only `element` and imports any deps from esm.sh.
export type LoopApi = {
  element: ToolElement;
  repo: Repo;
  subscribe: typeof subscribe;
  accept: typeof accept;
  loadSkill: (name: string) => string;
  writeFile: (path: string, content: string) => Promise<void>;
  readFile: (path: string) => Promise<string | undefined>;
  listFiles: () => Promise<string[]>;
  // Write the card's plain-language spec (a markdown doc the user can read).
  writeSpec: (markdown: string) => Promise<void>;
  giveUp: (reason: string) => never;
  console: CapturedConsole;
};

// The shape the generated effect module must export: a default function that
// receives the card's element and returns an optional cleanup.
export type EffectModule = {
  default: (element: ToolElement) => (() => void) | void;
};
